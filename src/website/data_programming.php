<h4>Conundrum   (C Programming)</h4>
<p>This event is aimed at finding the best minds in the country with apt programming tastes and problem solving skills.</p>
<p>Details for the Programming event are as follows :</p>
<ul class="items_list">
  <li>The language used will be C.</li>
  <li>Programming environment will be Windows</li>
  <li>Turbo C/C++ compiler will be provided</li>
  <li>Two members per team.      </li>
  <li>Any number of teams can participate from a college.</li>
  <li>Prelims would be conducted and 5 teams will be selected to the finals.</li>
  <li>The prelims would consist of  30 objective questions to be completed in 30 minutes.</li>
  <li>In the finals, the teams will be required to find the solution to a problem in 2 hours using the provided tools.</li>
  <li>The total prize money for the event will be Rs 5000/-</li>
</ul>
